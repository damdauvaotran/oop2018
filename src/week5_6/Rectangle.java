package week5_6;

public class Rectangle extends Shape {
	private int width = 0;
	private int height = 0;
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	
	
}	
